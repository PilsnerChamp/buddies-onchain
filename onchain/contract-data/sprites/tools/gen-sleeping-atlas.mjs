#!/usr/bin/env node
// Deterministic generator for plugin/src/sleeping-atlas.ts.
//
// Reads onchain/contract-data/sprites/buddies-source.mjs and emits the frame-0
// body slice + hat rows the plugin uses to render the local sleeping sprite.
// Source fixed -> regeneration is byte-stable (no-op).
//
// Cross-domain parity: the atlas mirrors what BuddyRenderer.sol reads via
// BuddySpriteData.getBodyRow(species, 0, row) / getHatRow(hat). The plugin
// applies the same row-0 hat injection + eye replacement (`0` -> `-`) the
// contract uses for the blink frame `fb`.
//
// Invariants enforced here (codegen-time only — parity test cannot catch them
// because both sides replace `0` before comparison):
//   1. Hat rows contain no `0` placeholder (the eye sentinel).
//   2. Each frame-0 contains >= 1 `0` placeholder (otherwise the sleeping
//      buddy renders without `-` eyes).
//   3. Frame-0 row 0 is either all-blank (hat-eligible slot) OR contains
//      `0` (species draws its face on row 0). Mixed content is rejected.

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath, pathToFileURL } from "node:url";
import { dirname, resolve } from "node:path";

const HERE = dirname(fileURLToPath(import.meta.url));
const SOURCE_PATH = resolve(HERE, "..", "buddies-source.mjs");
const REPO_ROOT = resolve(HERE, "..", "..", "..", "..");
const OUT_PATH = resolve(REPO_ROOT, "plugin", "src", "sleeping-atlas.ts");

class GenError extends Error {}

function rowIsBlank(row) {
  for (const ch of row) if (ch !== " ") return false;
  return true;
}

function validate(source) {
  const { SPECIES_ORDER, HAT_ORDER, bodySprites, hats } = source;

  for (const [hatName, hatRow] of Object.entries(hats)) {
    if (hatRow.includes("0")) {
      throw new GenError(`hat "${hatName}" contains forbidden eye placeholder \`0\``);
    }
  }

  for (const species of SPECIES_ORDER) {
    const frame0 = bodySprites[species].frames[0];
    const totalEyes = frame0.reduce((n, r) => n + (r.match(/0/g)?.length ?? 0), 0);
    if (totalEyes === 0) {
      throw new GenError(`${species} frame 0 has no \`0\` eye placeholder — sleeping render would be eyeless`);
    }

    const row0 = frame0[0];
    const row0HasEye = row0.includes("0");
    const row0Blank = rowIsBlank(row0);
    if (!row0Blank && !row0HasEye) {
      throw new GenError(
        `${species} frame 0 row 0 is non-blank with no \`0\` — neither hat-eligible nor face-bearing (got "${row0}")`
      );
    }
  }

  for (const hat of HAT_ORDER) {
    if (!Object.prototype.hasOwnProperty.call(hats, hat)) {
      throw new GenError(`HAT_ORDER lists "${hat}" but hats[${hat}] is missing`);
    }
  }
}

function literal(s) {
  return JSON.stringify(s);
}

function emit(source) {
  const { SPECIES_ORDER, HAT_ORDER, bodySprites, hats } = source;

  const lines = [];
  lines.push("// AUTOGENERATED by onchain/contract-data/sprites/tools/gen-sleeping-atlas.mjs.");
  lines.push("// DO NOT EDIT BY HAND. Edit onchain/contract-data/sprites/buddies-source.mjs and regenerate.");
  lines.push("//");
  lines.push("// Frame-0 body slice + hat rows for the local sleeping render. Mirrors what");
  lines.push("// BuddySpriteData.getBodyRow(species, 0, row) and getHatRow(hat) return on-chain.");
  lines.push("");
  lines.push('import type { Hat, Species } from "./bone-deriver";');
  lines.push("");
  lines.push("export const BODY_FRAME_0: Record<Species, readonly string[]> = {");
  for (const species of SPECIES_ORDER) {
    const rows = bodySprites[species].frames[0];
    lines.push(`  ${species}: [`);
    for (const row of rows) lines.push(`    ${literal(row)},`);
    lines.push(`  ],`);
  }
  lines.push("};");
  lines.push("");
  lines.push("export const HAT_ROWS: Record<Hat, string> = {");
  for (const hat of HAT_ORDER) {
    lines.push(`  ${hat}: ${literal(hats[hat])},`);
  }
  lines.push("};");
  lines.push("");

  return lines.join("\n");
}

async function main() {
  const args = process.argv.slice(2);
  const checkOnly = args.includes("--check");

  const source = await import(pathToFileURL(SOURCE_PATH).href);
  validate(source);
  const next = emit(source);

  if (checkOnly) {
    let current = "";
    try { current = readFileSync(OUT_PATH, "utf8"); } catch { /* missing is drift */ }
    if (current !== next) {
      console.error(`drift: ${OUT_PATH} differs from generator output. Run \`just sleeping-atlas-gen\`.`);
      process.exit(1);
    }
    console.log(`ok: ${OUT_PATH} matches generator.`);
    return;
  }

  writeFileSync(OUT_PATH, next, "utf8");
  console.log(`wrote ${OUT_PATH}`);
}

main().catch((err) => {
  if (err instanceof GenError) {
    console.error(`error: ${err.message}`);
    process.exit(1);
  }
  throw err;
});
